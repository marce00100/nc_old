                                <p>&ldquo;Hay dos testigos muy importantes que narran pr&aacute;cticamente muchos sucesos de lo que pas&oacute; ese d&iacute;a, con lo que se constituir&iacute;an en testigos presenciales; y creo que eso nos ha ayudado bastante en la investigaci&oacute;n (&hellip;) son gente del lugar y son testigos bien importantes, han dado por menores y los queremos proteger a nuestros testigos&rdquo;, afirm&oacute; ayer el fiscal Blanco, en conferencia de prensa. </p>
                                <p>Por otro lado, asegur&oacute; que se tienen individualizados a quienes el 25 de agosto, en la localidad de Panduro, arremetieron a golpes contra Illanes y le causaron la muerte.</p>
                                <p>Dentro de la investigaci&oacute;n, inform&oacute; que reci&eacute;n se colect&oacute; los documentos de identidad del exviceministro, como su cedula de identidad y credenciales del fallecido, que estaban ocultos en un matorral, en una lugar aleda&ntilde;o a donde fue victimado. Tambi&eacute;n se tienen videos y celulares.</p>
                                <p>DECLARACI&Oacute;N</p>
                                <p>En una confusa declaraci&oacute;n, donde afirma tener informaci&oacute;n pero no la puede dar, Blanco tampoco precis&oacute; si la interpelaci&oacute;n que la Asamblea Legislativa har&aacute; hoy a los ministros de Gobierno y Miner&iacute;a ser&aacute; tomada como indicios dentro de la investigaci&oacute;n.</p>
                                <p>El fiscal nuevamente rechaz&oacute; que el Ministerio P&uacute;blico haya filtrado videos que muestran al exviceministro Illanes antes de que sea asesinado. Agreg&oacute; que la filtraci&oacute;n de videos la realizaron algunos mineros que fueron aprehendidos, quienes entregaron las grabaciones a terceras personas.</p>
                                <p>A la fecha, 10 mineros cooperativistas guardan detenci&oacute;n preventiva en centros carcelarios, sindicados del asesinato del exviceministro.</p>
                                <p>Blanco confirm&oacute; que el Comando Departamental de la Polic&iacute;a entreg&oacute; ya un informe en el que est&aacute;n las im&aacute;genes del dron que la Polic&iacute;a utiliz&oacute; en el conflicto para el control de la represi&oacute;n contra los cooperativistas.</p>
                                <p>&ldquo;Una vez que el fiscal haga la valoraci&oacute;n respectiva, elaborar&aacute; la n&oacute;mina de polic&iacute;as que ser&aacute;n convocados a declarar, en calidad de testigos, para llegar a la verdad de ese hecho&rdquo;,agreg&oacute;.</p>
                                <p>Blanco refiri&oacute; tambi&eacute;n que los peritos realizan la investigaci&oacute;n t&eacute;cnica sobre el disparo de arma de fuego por el que murieron los mineros cooperativistas.</p>
                                <p>&ldquo;En el caso del minero que ha fallecido tenemos la lista, estamos analizando y una vez que declaren vamos a cruzar la informaci&oacute;n para poder determinar (responsabilidades)&rdquo;, se&ntilde;al&oacute; Blanco.</p></div> 
